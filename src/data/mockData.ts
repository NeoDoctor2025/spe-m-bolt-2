export function generateMockEvaluations(
  patients: { id: string; full_name: string; classification: string }[]
): Omit<Evaluation, 'user_id'>[] {
  const statuses: Evaluation['status'][] = ['Pendente', 'Em Andamento', 'Concluído'];
  const evaluations: Omit<Evaluation, 'user_id'>[] = [];

  patients.forEach((patient, idx) => {
    const count = idx % 3 === 0 ? 2 : 1;
    for (let i = 0; i < count; i++) {
      const status = statuses[(idx + i) % 3];
      const maxScore = 60;
      const totalScore = status === 'Concluído' ? Math.floor(Math.random() * 30) + 30 : 0;
      const daysAgo = Math.floor(Math.random() * 90);
      const date = new Date();
      date.setDate(date.getDate() - daysAgo);

      evaluations.push({
        id: crypto.randomUUID(),
        patient_id: patient.id,
        status,
        total_score: totalScore,
        max_score: maxScore,
        current_step: status === 'Concluído' ? 4 : status === 'Em Andamento' ? Math.floor(Math.random() * 4) : 0,
        started_at: date.toISOString(),
        completed_at: status === 'Concluído' ? new Date(date.getTime() + 3600000).toISOString() : null,
        created_at: date.toISOString(),
        updated_at: date.toISOString(),
        patient: patient as Evaluation['patient'],
      });
    }
  });

  return evaluations;
}
